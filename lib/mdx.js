export async function getAllAuthors(type) {
  let authors = {}

  const files = await getFiles(type)

  for (const f in files) {
    const file = files[f]
    const source = fs.readFileSync(path.join(root, 'data', type, file), 'utf8')
    const { data } = matter(source)
    if (data.authors && data.draft !== true) {
      for (const a in data.authors) {
        const author = data.authors[a]
        if (author in authors) {
          authors[author]['count'] += 1
        } else {
          let authorDetails = await getFileBySlug('authors', [author])
          console.log(authorDetails)
          authors[author] = {
            count: 1,
            name: authorDetails.frontMatter.name,
          }
        }
      }
    }
  }

  return authors
}

export async function getPostsByAuthor(author) {
  const prefixPaths = path.join(root, 'data/blog')

  const files = getAllFilesRecursively(prefixPaths)

  const allFrontMatter = []
  files.forEach((file) => {
    // Replace is needed to work on Windows
    const fileName = file.slice(prefixPaths.length + 1).replace(/\\/g, '/')
    // Remove Unexpected File
    if (path.extname(fileName) !== '.md' && path.extname(fileName) !== '.mdx') {
      return
    }
    const source = fs.readFileSync(file, 'utf8')
    const { data: frontmatter } = matter(source)
    if (frontmatter.authors.includes(author)) {
      allFrontMatter.push({
        ...frontmatter,
        slug: formatSlug(fileName),
        date: frontmatter.date ? new Date(frontmatter.date).toISOString() : null,
      })
    }
  })

  return allFrontMatter.sort((a, b) => dateSortDesc(a.date, b.date))
}

